package common;

public class Date {
    int jour, mois, annee;

    public Date( int jour, int mois, int annee){
        this.jour = jour;
        this.mois = mois;
        this.annee = annee;
    }

    public Date(){
        this.jour = 0;
        this.mois = 0;
        this.annee = 0;
    }
}


